// Service Worker for BrewIt - Coffee Recipe Hub
const CACHE_NAME = 'brewit-v1.0.7';
const urlsToCache = [
  './',
  './index.html',
  './src/script/script.js',
  './src/styles/styles.css',
  './manifest.json',
  './public/images/coffeebeans.jpg'
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', (event) => {
  // Skip non-GET requests and unsupported URL schemes
  if (event.request.method !== 'GET' || 
      event.request.url.startsWith('chrome-extension://') ||
      event.request.url.startsWith('moz-extension://') ||
      event.request.url.startsWith('safari-extension://')) {
    return;
  }

  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        // Return cached version or fetch from network
        if (response) {
          return response;
        }

        // For CSS files with cache-busting parameters, try to fetch without parameters first
        if (event.request.url.includes('styles.css')) {
          const baseUrl = event.request.url.split('?')[0];
          return fetch(baseUrl)
            .then((networkResponse) => {
              // Cache the response for future use
              if (networkResponse.status === 200) {
                const responseToCache = networkResponse.clone();
                caches.open(CACHE_NAME)
                  .then((cache) => {
                    cache.put(event.request, responseToCache);
                  });
              }
              return networkResponse;
            })
            .catch(() => {
              // If network fetch fails, try the original request
              return fetch(event.request);
            });
        }

        return fetch(event.request);
      })
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME) {
            console.log('Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});

// Handle background sync for offline functionality
self.addEventListener('sync', event => {
  if (event.tag === 'background-sync') {
    event.waitUntil(doBackgroundSync());
  }
});

